def png_image_to_text(png_path, line_size=80, separator="\n"):
    data = open(png_path, 'rb').read()
    compressed = binascii.b2a_base64(zlib.compress(data)).decode("ascii")
    formatted = separator.join(compressed[i:i + line_size] for i in range(0, len(compressed), line_size))
    return formatted


def text_image_to_bytes(img_text):
    cleared = img_text.replace("\n", "")
    return zlib.decompress(binascii.a2b_base64(cleared.encode("ascii")))


def qt_icon_from_text_image(img_text):
    data = text_image_to_bytes(img_text)
    qp = Qg.QPixmap()
    qp.loadFromData(data)
    icon = Qg.QIcon()
    icon.addPixmap(qp)
    return icon


def save_icon_as_png(in_icon_data, in_png_path):
    icon_data = text_image_to_bytes(in_icon_data)
    with open(in_png_path, 'wb') as fd:
        fd.write(icon_data)
        quit()


def boolean_string(s):
    if s not in {'False', 'True'}:
        raise ValueError('Not a valid boolean string')
    return s == 'True'


if __name__ == '__main__':
    parser = argparse.ArgumentParser(formatter_class=argparse.ArgumentDefaultsHelpFormatter,
                                     description='Convert image to text files. Which can be embedded in python'
                                                 'scripts to use as Qt icons')
    parser.add_argument('-i', '--input', help='path to the input image file', type=str, metavar='\b', required=True)
    parser.add_argument('-s', '--seperator', help='seperator used to separate line in the text file', type=str,
                        default="\n", metavar='\b')
    parser.add_argument('-l', '--line_size', help='number of characters per line', type=int,
                        default=120, metavar='\b')
    parser.add_argument('-o', '--output', help='output a text file, otherwise display text', type=boolean_string,
                        default=True, metavar='\b')

    args = parser.parse_args()

    if not path.isfile(args.input):
        print(f"No file found under: {args.input}")
        exit(0)

    data_text = png_image_to_text(args.input, args.line_size, args.seperator)

    if args.output:
        with open(args.input + ".txt", 'w') as f:
            f.write(data_text)
    else:
        print(data_text)
